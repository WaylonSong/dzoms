SELECT     t1.编号, t1.档案号, t1.车号, t1.发动机号, t1.车架号, t1.购车时间, t1.发票号, t1.合格证号, t1.拍卖登记号, t1.营运证发放日期, t1.营运证号, t1.计价器号, 
                      t1.经营权编码, t1.经营权开始日期, t1.经营权终止日期, t1.分公司归属, t2.车辆制造企业名称, t2.车辆型号, t2.归属部门, t1.承租人, t1.承租人身份证号, 
                      t1.牌照类别
FROM         车辆基本信息表 AS t1 INNER JOIN
                      车辆基本信息表_大众 AS t2 ON t1.编号 = t2.编号
WHERE     (t1.报废状态 <> 'True')